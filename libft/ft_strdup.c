#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*str;
	int		dup_len;
	int		i;

	dup_len = ft_strlen(s);
	str = (char *) malloc((dup_len + 1) * sizeof(char));
	if (str == NULL)
		return (NULL);
	i = 0;
	while (s[i] != '\0')
	{
		str[i] = s[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
